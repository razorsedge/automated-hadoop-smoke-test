#!/bin/bash
source ./conf/SmokeConfig.config

	if $SECURITY ; then
		kinit -kt $KRB_KEYTAB $KRB_PRINCIPAL
	fi

	if $HDFS ; then
		hdfs dfs -rm $HDFS_PATH
		rm -f $TEMP_PATH
		echo "******************************************************************************************************************************************"
	fi

	if $MAPREDUCE ; then

		hdfs dfs -rm -r $MAP_REDUCE_OUT
		echo "******************************************************************************************************************************************"
	fi

	if $HIVE ; then

		bash ./bin/hivedrop.sh
		echo "******************************************************************************************************************************************"
	fi

	if $HBASE ; then

		hbase shell -n  ./lib/hbase_rm.txt
		#rm -f /tmp/hbase.$$ /tmp/hbase-rm.$$
		echo "******************************************************************************************************************************************"
	fi

	if $SPARK2 ; then

		hdfs dfs -rm -R $SPARK_OUT_CLUS
		hdfs dfs -rm -R $SPARK_IN_CLUS
		echo "******************************************************************************************************************************************"
	fi

	if $SPARK ; then

		hdfs dfs -rm -R $SPARK_OUT_CLUS
		hdfs dfs -rm -R $SPARK_IN_CLUS
		echo "******************************************************************************************************************************************"
	fi

	if $PIG ; then

		hdfs dfs -rm -R $PIG_PATH_OUT
		hdfs dfs -rm -R $SPARK_OUT_CLUS
		echo "******************************************************************************************************************************************"
	fi

	if $KAFKA ; then

		$KAFKA_HOME/kafka-topics.sh --zookeeper ${ZOOKEEPER} --delete --topic test
		echo "******************************************************************************************************************************************"
	fi

	if $SOLR ; then

		bash ./bin/solr_rm.sh
		echo "******************************************************************************************************************************************"

	fi

	if $SECURITY ; then
		kdestroy
	fi

